        end_loc = wbs[wbs["Activity"] == options["Activity that Marks End of Location"]].index.astype(int)


        if len(sequence_list) < len(start_loc):
            print("Oh ohh: we're in trouble here, the sequence is shorter than the number of location visited")
            print("Aventis doesn't know what to do :-(, all wtg locations are now 'Undefined'")
            sequence_implemented = False

            return wbs, sequence_implemented


        elif len(sequence_list) > len(start_loc):

            print("Sequence is longer than number of locations visited")
            num_used = len(start_loc)
            num_skipped = len(sequence_list) - num_used
            print("Aventis will finish skip the first ", num_skipped, " locations")
