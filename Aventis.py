def f_prepare_and_return_derived_bool(timeseries_input, unique_DB, weather_ID, weather):
    """prepare things to send to derive bool- a stepping stone function"""

    # input for f_derive_bool
    index_of_pairs = duplicates(list(timeseries_input["DB ID"]), unique_DB)
    num_compare = len(index_of_pairs)
    params = timeseries_input["Parameters"][index_of_pairs]
    param_lims = timeseries_input["Param Limits"][index_of_pairs]
    param_tss = f_retrieve_param_ts(weather, params, num_compare, weather_ID)

