def f_derive_parameters(weather_aux, dp_individual, weather_ID, dp_ID):
    """reads the forumlas in the derived parameters column and runs them in python"""
    # Split the written forumla in to individual components, then
    # change the param ids in to strings that call the param columns
    # eg 'Hs' --> 'weather_aux['Hs']''
    # join the list back in to a string
    # eg 'Hs * Tp ** 2'
    # finally evaluate and save as a new column (column name is DP ID)
    dp = []
    aux = []
    for val in dp_individual:

        if val in weather_aux.columns.values.tolist():
            aux_list = "".join(["weather_aux['",val,"']"])
            aux.append("".join(aux_list))

        else:
            aux.append(val)


    programme = "".join(aux)
    #print(weather_aux)
    try:
        dp = eval(programme)
    except:
        print("Warning: one of the parameters in ", dp_individual, "is named in ", weather_ID,  "DP Formula column but is not in the actual pickle/csv")
    
    weather_aux[dp_ID] = dp

    return weather_aux

