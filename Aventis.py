def f_split_input_sheet():
    """find inds of input sheet:
    -options start and ends
    -weather start and ends
    -wbs begins
    use these later to load up relevent parts of sheet"""
    split_dict = {}
    filepath = askopenfilename()
    df = pd.read_excel(filepath, sheet_name="Simulation")

    wbs_start = df[df["Aventis"] == "Workflow"].index.values[0]+2

    #Find end weather
    i = 3
    while pd.isnull(df["Aventis"][wbs_start-i]):
        i += 1
    end_weather = wbs_start - i - 1

    #Find start weather
    i = 1
    while df["Aventis"][end_weather-i] != "Weather":
        i += 1
    start_weather = end_weather - i

    #Find end Output options
    i = 2
    while pd.isnull(df["Aventis"][start_weather-i]):
        i += 1
    end_out_options = start_weather-i-1

    #Finding Sequence Options
    i = 1
    while df["Aventis"][end_out_options-i] != "Options":
        i += 1
    first_options = end_out_options-i

    #Finding simulation info
    i = 1
    while pd.isnull(df["Aventis"][first_options-i]):
        i += 1
    last_simulation = first_options-i-1

