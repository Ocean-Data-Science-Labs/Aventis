def f_continuous_with_previous(wbs, options, window_bool_dict):
    """ check continuous condition and create new booleans careful, 
    this only works if the required weather windows are longer 
    than the activity net duration

    inputs wbs, options and bool_dict
    output wbs and bool_dict with added keys and values"""


    # find the activities that are continuous with each other by finding all Yes and using 
    # the diff function on the activity index
    continuous_with_prev = list(compress(range(len(wbs["Continuous With Previous"] == "Yes")), 
                                         wbs["Continuous With Previous"] == "Yes"))
    continuous_with_prev_np = np.asarray(continuous_with_prev)
    con_diff = np.diff(continuous_with_prev_np)
    np.insert(con_diff, 1, 0)

    # initialise some lists and a counter (the counter is for naming the dict key)
    list_of_acts = list()
    list_of_durs = list()
    list_of_bools = list()
    counter = 0

    # okay- so now we're going to loop through the continuous activities, if the difference is == 1
    # then we know that its continuous with the last activity so we'll mark which activity it was
    # if the differnence is not one then we know that it is no longer a continuous set, so we're gonna
    # create a staggered boolean array and read if all are ones in each row (where 1 = workable), if there are
    # any zeros then we'll mark it as non-workable. The array is staggered by net duration of the previous activity
    # This quick method is why the weather window must be greater than the activity duration to work properly

    for i in range(0, len(con_diff)):

        # If continuous with each other
        if con_diff[i] == 1:
            list_of_acts.append(continuous_with_prev[i])
            list_of_durs.append(wbs["Duration [timesteps]"].loc[continuous_with_prev[i]])
            list_of_bools.append(wbs["Weather Boolean Key"].loc[continuous_with_prev[i]])


        # if not continuous then find the windows of all the listed activities    
        if con_diff[i] != 1:
            

            list_of_acts.append(continuous_with_prev[i])
            list_of_durs.append(wbs["Duration [timesteps]"].loc[continuous_with_prev[i]])
            list_of_bools.append(wbs["Weather Boolean Key"].loc[continuous_with_prev[i]])

            bool_array = np.zeros([len(list_of_bools), len(window_bool_dict[0])])

            inds = np.zeros([len(list_of_bools)])

            # create bool array
            for j in range(0, len(list_of_bools)):

                if j == 0:
                    aux = window_bool_dict[list_of_bools[j]]
                else:   
                    aux = window_bool_dict[list_of_bools[j]][int(sum((list_of_durs[:j]))):]

                    aux = np.append(aux, np.ones(int(sum(list_of_durs[:j]))))


                bool_array[j,:] = aux


            cont_bool_array = bool_array.all(axis=0)



            window_bool_dict["".join(["C", str(counter)])] = cont_bool_array           
            wbs["Weather Boolean Key"].loc[list_of_acts[0]] = "".join(["C", str(counter)])

            list_of_acts = list()
            list_of_durs = list()
            list_of_bools = list()

            counter += 1

        
    return wbs, window_bool_dict

