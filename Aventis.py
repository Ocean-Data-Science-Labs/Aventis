from math import sin, cos, sqrt, atan2, radians

def f_distance_between_two_lat_lon(lat1, lon1, lat2, lon2):
    """distance between two lat lons
    convers lat lons in to radians first
    from:
    https://stackoverflow.com/questions/19412462/
    getting-distance-between-two-points-based-on-latitude-longitude

    returns a distance in km"""

    # approximate radius of earth in km
    R = 6373.0

    lat1_r = radians(lat1)
    lon1_r = radians(lon1)
    lat2_r = radians(lat2)
    lon2_r = radians(lon2)

    dlon = lon2_r - lon1_r
    dlat = lat2_r - lat1_r

    a = sin(dlat / 2)**2 + cos(lat1_r) * cos(lat2_r) * sin(dlon / 2)**2
    c = 2 * atan2(sqrt(a), sqrt(1 - a))

    # find distance in km
    distance = R * c

    return distance

def f_convert_transits_distances_to_durations(options, wbs, timestep, sequence, sequence_implemented, wtg_properties):
    """ If a transit speed is given then calculate the net durations [timesteps] with
    the transit speeds (intra and inter-site). Does not change the minimum start window!

    options - dict of options read from input sheet
    wbs - dataframe of work breakdown structure
    timestep - float timestep in hours
    sequence_implemented - boolean, where True means a sequence is used"""

    if options["Transit Speed [kts]"] > 0:

        kts_to_kmh = 1.58 #translate kts to kmh

        # calculate iter-site transits
        transit_other_loc = wbs[wbs["Location"].str.contains("Transit - Inter Site")].index.astype(int)
        wbs.loc[transit_other_loc, ("Duration [timesteps]")] =  ((wbs["Net Duration [hrs]"][transit_other_loc]
                                                                / (options["Transit Speed [kts]"] * kts_to_kmh))
                                                                / timestep).round().astype(int)


        # change minimum window duration [timesteps] to maintain the ratio of hours to
        #wbs.loc[transit_other_loc, ("Minimum Window Duration [timesteps]")] = (wbs["Net Duration [hrs]"][transit_other_loc]
        #                                                                       / wbs["Minimum Window Duration [hrs]"][transit_other_loc]
        #                                                                       * wbs["Duration [timesteps]"][transit_other_loc]

        # calculate intra-site transits
    if options["Intra-Site Transit Speed [kts]"] > 0:    
        if sequence_implemented:
            sequence_list = list(sequence["Simulation Sequence ID"])

            # find the indicies of the transits
            transit_intra_loc = wbs[wbs["Location"].str.contains("Transit - Intra Site")].index.astype(int)

            # find the indicies of the indicies either side of the transit
            prev_loc = transit_intra_loc - 1
            next_loc = transit_intra_loc + 1

            # find the named locations either side of the transit
