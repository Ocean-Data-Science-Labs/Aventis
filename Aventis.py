def f_construct_wbs(split_dict):
    """Effectively a running script that reads input sheet and constructs WBS """

    df = pd.read_excel(split_dict["Filepath"], sheet_name="Simulation",
                       skiprows=split_dict["First WBS"])
    df = df.fillna("NA")

    fragnet_inds, fragnet_names = f_find_fragnets_inds(df)

    #
    try:
        wbs_aux = df[df.index.isin(range(0,fragnet_inds[0][0] - 2 ))]

    except:
        wbs_aux = df

    #
    wbs = pd.DataFrame()

    unwrapped_fragnet_dict = {}

    for i in range(0, len(fragnet_names)):
        fragnet_name_aux = fragnet_names[fragnet_inds[0][i]]
        df_fragnet_aux = df.loc[fragnet_inds[0][i]+1: fragnet_inds[1][i]-2]
        df_fragnet = pd.DataFrame()
        while df_fragnet_aux["Activity"].isin(fragnet_names).any():
            df_fragnet_aux = f_one_wbs_sweep_of_fragnet(df_fragnet, df_fragnet_aux, fragnet_names, fragnet_inds, df)


        unwrapped_fragnet_dict[fragnet_name_aux] = df_fragnet_aux


    wbs = f_one_wbs_sweep(wbs, wbs_aux, unwrapped_fragnet_dict, fragnet_names)    
    wbs_final = f_iron_out_multiples_wbs(wbs)

    #Post wbs creation tid-bits
    wbs_final, timestep, error_bool = f_find_timestep(wbs_final)
    if error_bool == False:
        weather_IDs = f_find_unique_weather_IDs(wbs_final)
    
    else:
        weather_IDs = 'NA'
        
    return wbs_final, timestep, weather_IDs, error_bool

