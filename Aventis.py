def duplicates(lst, item):
    """find all duplicates in an array"""
    return [i for i, x in enumerate(lst) if x == item]


def f_retrieve_param_ts(weather, params, num_compare, weather_ID):
    """create a numpy array of the timeseries (ts) of each parameters required
    to create the bool"""
    param_vals = np.zeros([num_compare, len(weather["".join([weather_ID, "_data"])].index)])
    i = 0
    for param in params:
        param_vals_aux = np.asarray(weather["".join([weather_ID, "_data"])][param])
