def f_complete_activity(duration, ts, weather_bool, weather_bool2):
    """Complete next activity
    weather_bool is a bool that has already checked the windows
    window_bool is a bool of if it can work or not
    idea is that we check for the next window then jump to the end of the period of 1s in weather_bool2"""

    while duration > 0:
        #find the index of the next workable window then find the 
        #index of the last working timestamp after that
        ts_aux = find_first(1, weather_bool[ts:])
        
        ntw = np.minimum(find_first(0, weather_bool2[ts+ts_aux:]), duration) 

        duration -= ntw
        ts += int(ts_aux + ntw)
        
        
    return ts

